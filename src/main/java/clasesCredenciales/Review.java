package clasesCredenciales;

/**
 *
 * @author JB
 */
public class Review {
    
    private String author;
    
    private String body;

    public Review(String author, String body) {
        this.author = author;
        this.body = body;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
    
}
